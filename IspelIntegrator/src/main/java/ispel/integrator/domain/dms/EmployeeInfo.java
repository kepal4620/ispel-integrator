package ispel.integrator.domain.dms;

public class EmployeeInfo {

    private String uzivatelske_meno;

    private String prijmeni;
    private String jmeno;

    public String getUzivatelske_meno() {
        return uzivatelske_meno;
    }

    public void setUzivatelske_meno(String uzivatelske_meno) {
        this.uzivatelske_meno = uzivatelske_meno;
    }

    public String getPrijmeni() {
        return prijmeni;
    }

    public void setPrijmeni(String prijmeni) {
        this.prijmeni = prijmeni;
    }

    public String getJmeno() {
        return jmeno;
    }

    public void setJmeno(String jmeno) {
        this.jmeno = jmeno;
    }
}
